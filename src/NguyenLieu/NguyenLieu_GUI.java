    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int rowIndex = jTable2.getSelectedRow();
        if (rowIndex != -1) {
            Object idm = jTable2.getValueAt(rowIndex, 0); // Lấy giá trị ID từ cột đầu tiên của dòng được chọn
            if (idm != null) { // Kiểm tra xem ID có tồn tại không
                int id = Integer.parseInt(idm.toString()); // Chuyển đổi ID thành kiểu int
                boolean result = NguyenlieuDAO.xoanguyenlieu(id); // Gọi phương thức xóa món ăn với ID vừa lấy được
                if (result) {
                    JOptionPane.showMessageDialog(null, "Xóa thành công!");
                    load_tableMon(new NguyenlieuDAO().laydulieunguyenlieu());
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa thất bại!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Không có ID để xóa!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để xóa!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String tenNguyenLieu = jTextField2.getText();
        String maNguyenLieuStr = jTextField3.getText(); // Giả sử bạn có một jTextField để nhập mã nguyên liệu
        String soLuongTonStr = jTextField1.getText();

        // Validate that the ingredient name, ID, and quantity are not empty
        if (tenNguyenLieu != null && !tenNguyenLieu.isEmpty()
                && maNguyenLieuStr != null && !maNguyenLieuStr.isEmpty()
                && soLuongTonStr != null && !soLuongTonStr.isEmpty()) {

            try {
                // Convert the ID to an integer
                int maNguyenLieu = Integer.parseInt(maNguyenLieuStr);
                // Convert the quantity to an integer
                int soLuongTon = Integer.parseInt(soLuongTonStr);

                // Call the method to update the ingredient information
                boolean result = NguyenlieuDAO.capNhatnguyenlieu(maNguyenLieu, tenNguyenLieu, soLuongTon);

                // Show a message indicating whether the update was successful
                if (result) {
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
                    load_tableMon(NguyenlieuDAO.laydulieunguyenlieu());
                } else {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại!");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Mã nguyên liệu và số lượng tồn phải là số nguyên hợp lệ!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên, mã nguyên liệu và số lượng tồn để cập nhật!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

