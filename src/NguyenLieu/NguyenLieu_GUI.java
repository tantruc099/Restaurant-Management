    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String maNguyenLieuText = jTextField3.getText().trim();
        String tenNguyenLieuText = jTextField2.getText().trim();

        Integer maNguyenLieu = null;
        if (!maNguyenLieuText.isEmpty()) {
            try {
                maNguyenLieu = Integer.parseInt(maNguyenLieuText);
            } catch (NumberFormatException e) {
                // Handle invalid integer input
                JOptionPane.showMessageDialog(this, "Mã nguyên liệu phải là số nguyên.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        String tenNguyenLieu = null;
        if (!tenNguyenLieuText.isEmpty()) {
            tenNguyenLieu = tenNguyenLieuText;
        }

        // Call the search method
        ArrayList<Nguyenlieu> resultList = NguyenlieuDAO.timkiemNguyenlieu(maNguyenLieu, tenNguyenLieu);

        // Display results in the table
        displayResults(resultList);
    }//GEN-LAST:event_jButton5ActionPerformed

    private void displayResults(ArrayList<Nguyenlieu> resultList) {
        // Clear the existing rows in the table model
        dtm.setRowCount(0);

        // Populate the table model with the search results
        for (Nguyenlieu nl : resultList) {
            Vector<Object> row = new Vector<>();
            row.add(nl.getMaNguyenLieu());
            row.add(nl.getTenNguyenLieu());
            row.add(nl.getSoluongton());
            dtm.addRow(row);
        }

        // Update the table model
        jTable2.setModel(dtm);
    }
