    private void load_tableMon(ArrayList<Nguyenlieu> danhSach) {
        dtm.setRowCount(0);
        for (Nguyenlieu pb : danhSach) {
            Vector<Object> v = new Stack<>();
            v.add(pb.getMaNguyenLieu());
            v.add(pb.getTenNguyenLieu());
            v.add(pb.getSoluongton());
            dtm.addRow(v);
        }
        jTable1.setModel(dtm);
    }

