    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String manl = jTextField1.getText();
        int maNguyenlieu = Integer.parseInt(manl);
        String Tennl = jTextField2.getText();
        String SL = txtSoLuongNhap.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date NgayNhap = null;
        try {
            NgayNhap = dateFormat.parse(jFormattedTextField1.getText());
        } catch (ParseException ex) {
            Logger.getLogger(PhieuNhapKho.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (SL.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền số lượng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

//        PhieuNhapKho_1 pn = new PhieuNhapKho_1();
//        pn.setManguyenlieu(Integer.parseInt(manl));
//        pn.setSoluong(Integer.parseInt(SL));
//        pn.setTennguyenlieu(Tennl);
//        pn.setNgayNhap(NgayNhap);
//        list.add(pn);
//        load_tablePhieuNhapKho(list);
        int soluongNum = Integer.parseInt(SL);

        boolean found = false;
        for (int i = 0; i < list.size(); i++) {
            PhieuNhapKho_1 Bill2 = list.get(i);
            if (Bill2.getManguyenlieu() == maNguyenlieu) {
                // Mã món đã tồn tại, cập nhật số lượng và kết thúc vòng lặp
                Bill2.setSoluong(Bill2.getSoluong() + soluongNum);
                found = true;
                break;

            }
        }
        if (!found) {
            // Mã món không tồn tại, thêm món mới vào danh sách
            PhieuNhapKho_1 Ver2 = new PhieuNhapKho_1(maNguyenlieu, Tennl, soluongNum, NgayNhap);
            list.add(Ver2);
            Object[] rowData = {maNguyenlieu, Tennl, soluongNum, NgayNhap};
            dtm2.addRow(rowData);
            jTable3.setModel(dtm2);
            load_tablePhieuNhapKho(list);
        } else {
            load_tablePhieuNhapKho(list);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

