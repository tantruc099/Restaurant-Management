    private void load_tableMon(ArrayList<Nguyenlieu> danhSach) {
        dtm.setRowCount(0);
        for (Nguyenlieu pb : danhSach) {
            Vector<Object> v = new Stack<>();
            v.add(pb.getMaNguyenLieu());
            v.add(pb.getTenNguyenLieu());
            v.add(pb.getSoluongton());
            dtm.addRow(v);
        }
        jTable1.setModel(dtm);
    }
        private void load_tableKho(ArrayList<NhapKhoModel> danhSach) {
        dtm2.setRowCount(0);
        for (NhapKhoModel pb : danhSach) {
            Vector<Object> v = new Stack<>();
            v.add(pb.getMaNhapKho());
            v.add(pb.getMaNguyenLieu());
            v.add(pb.getSoLuongNhap());
            dtm2.addRow(v);
        }
        jTable2.setModel(dtm2);

    }
