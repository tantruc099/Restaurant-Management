    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String tenmon = jTextField3.getText();
    String soluong = jTextField4.getText();

    if (!tenmon.isEmpty() && !soluong.isEmpty()) {
        try {

            int maNguyenLieu = Integer.parseInt(tenmon);
            int soLuongNhap = Integer.parseInt(soluong);

            NhapKhoModel nhapkho = new NhapKhoModel();
            nhapkho.setMaNguyenLieu(maNguyenLieu);
            nhapkho.setSoLuongNhap(soLuongNhap);

            boolean result = NhapkhoDAO.insertKho(nhapkho);
            if (result) {
                JOptionPane.showMessageDialog(null, "Nhập kho thành công!");
                int currentId = GetId() + 1;
                load_tableKho(new NhapkhoDAO().laydulieuNhapKho());
                load_tableMon(new NguyenlieuDAO().laydulieunguyenlieu());
            } else {
                JOptionPane.showMessageDialog(null, "Nhập kho thất bại!");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Số lượng nhập không hợp lệ!");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin để thực hiện nhập kho!");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

