    private void load_tableMon(ArrayList<food> danhSach) {
        dtm.setRowCount(0);
        for (food pb : danhSach) {
            Vector<Object> v = new Stack<>();
            v.add(pb.getMaThucPham());
            v.add(pb.getTenThucPham());
            v.add(pb.getGia());
            v.add(pb.getGhiChu());
            dtm.addRow(v);
        }
        tb.setModel(dtm);

    }

