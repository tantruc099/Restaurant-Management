    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        String tenmon = txt_tenmon.getText();
        String gia = txt_gia.getText();
        String mota = txt_mt.getText();
        food mm = new food();
        mm.setTenThucPham(tenmon);

        mm.setGia(Float.valueOf(gia));
        mm.setGhiChu(mota);
        if (MenuFood.insertFood(mm)) {
            JOptionPane.showMessageDialog(this, "Them Thanh Cong", "Thong Bao", JOptionPane.INFORMATION_MESSAGE);
            load_tableMon(new MenuFood().layDmenumon());
        }

    }//GEN-LAST:event_btnthemActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        int rowIndex = tb.getSelectedRow();
        if (rowIndex != -1) {
            Object idMonan = tb.getValueAt(rowIndex, 0); // Lấy giá trị ID từ cột đầu tiên của dòng được chọn
            if (idMonan != null) { // Kiểm tra xem ID có tồn tại không
                int id = Integer.parseInt(idMonan.toString()); // Chuyển đổi ID thành kiểu int
                boolean result = MenuFood.xoaPmon(id); // Gọi phương thức xóa món ăn với ID vừa lấy được
                if (result) {
                    JOptionPane.showMessageDialog(null, "Xóa thành công!");
                    load_tableMon(new MenuFood().layDmenumon());
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa thất bại!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Không có ID để xóa!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để xóa!");
        }

    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        String tenmon = txt_tenmon.getText();
        String Mamon = txt_mm.getText();
        String gia = txt_gia.getText();
        String mota = txt_mt.getText();

        if (Mamon != null && !Mamon.isEmpty()) {
            
            boolean result = MenuFood.capNhatmonan(tenmon, Mamon, gia, mota);

            if (result) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
                load_tableMon(new MenuFood().layDmenumon());

            } else {
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một món ăn để cập nhật!");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        int rowIndex = tb.getSelectedRow();
        if (rowIndex != -1) {
            Object idMonan = tb.getValueAt(rowIndex, 0);
            Object Tenmonan = tb.getValueAt(rowIndex, 1);
            Object gia = tb.getValueAt(rowIndex, 2);
            Object mota = tb.getValueAt(rowIndex, 3);
            if (idMonan != null) { 
                String id = idMonan.toString();
                String tenmonan = Tenmonan.toString();
                String giaMon = gia.toString();
                String moTa = mota.toString();

                txt_mm.setText(id);
                txt_tenmon.setText(tenmonan);
                txt_gia.setText(giaMon);
                txt_mt.setText(moTa);
            }
        }


    }//GEN-LAST:event_tbMouseClicked

